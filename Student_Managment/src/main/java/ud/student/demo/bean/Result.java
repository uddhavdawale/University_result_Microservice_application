package ud.student.demo.bean;

import javax.persistence.Entity;
import javax.persistence.Id;


public class Result 
{
	
	private int PRN;	
	private float marathi;
	private float english;
	private float hindi;
	private float science;
	private float math;
	private float technology;
	
	public Result() {}
	public Result(int pRN, float marathi, float english, float hindi, float science, float math, float technology) {
		super();
		PRN = pRN;
		this.marathi = marathi;
		this.english = english;
		this.hindi = hindi;
		this.science = science;
		this.math = math;
		this.technology = technology;
	}
	public int getPRN() {
		return PRN;
	}
	public void setPRN(int pRN) {
		PRN = pRN;
	}
	public float getMarathi() {
		return marathi;
	}
	public void setMarathi(float marathi) {
		this.marathi = marathi;
	}
	public float getEnglish() {
		return english;
	}
	public void setEnglish(float english) {
		this.english = english;
	}
	public float getHindi() {
		return hindi;
	}
	public void setHindi(float hindi) {
		this.hindi = hindi;
	}
	public float getScience() {
		return science;
	}
	public void setScience(float science) {
		this.science = science;
	}
	public float getMath() {
		return math;
	}
	public void setMath(float math) {
		this.math = math;
	}
	public float getTechnology() {
		return technology;
	}
	public void setTechnology(float technology) {
		this.technology = technology;
	}
	
	
}
